package com.vanth.request_response;

public class DistanceRequestEntity {
	String id_vehicle;
	int month;
	int year;
	
	public String getId_vehicle() {
		return id_vehicle;
	}
	public void setId_vehicle(String id_vehicle) {
		this.id_vehicle = id_vehicle;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	
}
